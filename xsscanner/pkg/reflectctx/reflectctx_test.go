func TestClassifyContext_LeadingMarkerQuote(t *testing.T) {
	body := []byte(`<input value=""x9dbg" class="form-control">`)
	idx := strings.Index(string(body), "x9dbg")
	if idx == -1 {
		t.Fatal("canary not found")
	}
	ctx := ClassifyContext(body, idx)
	if ctx.Type != ContextHTMLAttrQuoted {
		t.Errorf("Expected ContextHTMLAttrQuoted, got %v", ctx.Type)
	}
	if ctx.QuoteChar != '"' {
		t.Errorf("Expected quote char '\"', got %v", ctx.QuoteChar)
	}

	confirmed, ctxType := VerifyBreakout(body, "x9dbg", '"')
	if !confirmed {
		t.Errorf("Expected confirmed true, got false")
	}
	if ctxType != ContextHTMLAttrQuoted {
		t.Errorf("Expected ContextHTMLAttrQuoted, got %v", ctxType)
	}
}

func TestClassifyContext_UnrelatedStrayQuote(t *testing.T) {
	// This proves the safety loop still catches a genuinely closed attribute.
	body := []byte(`<input value="foo"bar x9dbg" class="x">`)
	idx := strings.Index(string(body), "x9dbg")
	if idx == -1 {
		t.Fatal("canary not found")
	}
	ctx := ClassifyContext(body, idx)
	if ctx.Type != ContextUnknown {
		t.Errorf("Expected ContextUnknown, got %v", ctx.Type)
	}
	confirmed, _ := VerifyBreakout(body, "x9dbg", '"')
	if confirmed {
		t.Errorf("Expected confirmed false, got true")
	}
}

func BenchmarkClassifyContext(b *testing.B) {
	// Create a realistic 100KB body payload
	body := make([]byte, 100*1024)
	for i := range body {
		body[i] = ' '
	}

	// Inject the canary near the end to force backwards scanning
	copy(body[99000:], []byte("CANARY"))

	b.ResetTimer()
	for i := 0; i < b.N; i++ {
		// Target: should easily hit millions of iterations per second without GC thrash
		ClassifyContext(body, 99000)
	}
}
